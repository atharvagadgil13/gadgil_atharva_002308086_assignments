    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        if(!searchBox.getText().isBlank()){
            String searchParameter = searchBox.getText();
            Person foundPerson = personDirectory.searchPerson(searchParameter);
            if(foundPerson != null){
                ViewPersonJPanel panel = new ViewPersonJPanel(workArea, personDirectory, foundPerson);
                workArea.add("ViewPersonJPanel", panel);
                CardLayout layout = (CardLayout) workArea.getLayout();
                layout.next(workArea);
            }
            else{
                JOptionPane.showMessageDialog(null, "Account not found. Please check the account number and try again.","Warning",JOptionPane.WARNING_MESSAGE);
                
            }
        }

    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblPersons.getSelectedRow();
        if(selectedRow >= 0) {
            
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete the selected Person?", "Warning", dialogButton);
            if (dialogResult == JOptionPane.YES_OPTION){
                Person selectedPerson = (Person) tblPersons.getValueAt(selectedRow, 0);
                personDirectory.deletePerson(selectedPerson);
                populateTable();
            }
            else{
                JOptionPane.showMessageDialog(null, "Please select a Person from the list", "Warning", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

