    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblPersons.getModel();
          model.setRowCount(0);
        for(Person p : personDirectory.getPersons()){
            Object[] row = new Object[6];
            row[0] = p;
            row[1] = p.getLastName();
            row[2] = p.getHomeCity();
            row[3] = p.getHomeZip();
            row[4] = p.getOfficeCity();
            row[5] = p.getOfficeZip();
            model.addRow(row);
         }
    }
